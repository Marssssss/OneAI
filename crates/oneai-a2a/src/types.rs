// ─── AgentCard ──────────────────────────────────────────────────────────────────

/// AgentCard — the discovery/profile document that an agent publishes.
///
/// The AgentCard is the core metadata document in A2A. It describes:
/// - Who the agent is (name, description, provider)
/// - What it can do (skills)
/// - How to reach it (url)
/// - What features it supports (capabilities)
/// - How to authenticate (authentication)
///
/// A well-known endpoint (typically `/.well-known/agent-card`) serves
/// this document for agent discovery.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "camelCase")]
pub struct AgentCard {
    /// Human-readable name of the agent.
    pub name: String,
    /// Description of the agent's capabilities.
    pub description: String,
    /// The URL endpoint where the agent can be reached (A2A server).
    pub url: String,
    /// Version of the agent (e.g., "1.0.0").
    #[serde(skip_serializing_if = "Option::is_none")]
    pub version: Option<String>,
    /// Provider/organization info.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub provider: Option<AgentProvider>,
    /// Skills the agent offers.
    #[serde(default)]
    pub skills: Vec<AgentSkill>,
    /// Default content types for input.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub default_input_modes: Vec<String>,
    /// Default content types for output.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub default_output_modes: Vec<String>,
    /// Feature capabilities the agent supports.
    #[serde(default)]
    pub capabilities: AgentCapabilities,
    /// Authentication requirements.
    #[serde(default)]
    pub authentication: AuthenticationInfo,
    /// Optional URL to agent documentation.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub documentation_url: Option<String>,
}

/// Provider/organization info for an agent.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct AgentProvider {
    /// Organization name.
    pub organization: String,
    /// Organization URL.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub url: Option<String>,
}

impl AgentCard {
    /// Create a minimal AgentCard with name, description, and URL.
    pub fn new(name: impl Into<String>, description: impl Into<String>, url: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            description: description.into(),
            url: url.into(),
            version: None,
            provider: None,
            skills: Vec::new(),
            default_input_modes: vec!["text/plain".to_string()],
            default_output_modes: vec!["text/plain".to_string()],
            capabilities: AgentCapabilities::default(),
            authentication: AuthenticationInfo::default(),
            documentation_url: None,
        }
    }

    /// Parse an AgentCard from a JSON string.
    pub fn from_json(json: &str) -> Result<Self, crate::error::A2AError> {
        serde_json::from_str(json).map_err(|e| crate::error::A2AError::Serialization(e.to_string()))
    }

    /// Serialize this AgentCard to a JSON string.
    pub fn to_json(&self) -> Result<String, crate::error::A2AError> {
        serde_json::to_string(self).map_err(|e| crate::error::A2AError::Serialization(e.to_string()))
    }

    /// Serialize this AgentCard to a pretty-printed JSON string.
    pub fn to_json_pretty(&self) -> Result<String, crate::error::A2AError> {
        serde_json::to_string_pretty(self).map_err(|e| crate::error::A2AError::Serialization(e.to_string()))
    }
}

