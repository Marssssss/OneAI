//! A2A Protocol data types — complete type definitions per the Google A2A specification.
//!
//! The A2A (Agent-to-Agent) protocol defines a standardized way for AI agents
//! to discover each other's capabilities and collaborate on tasks. This module
//! implements all the core data types from the specification:
//!
//! - **AgentCard**: Discovery metadata describing an agent's capabilities
//! - **Task**: The central unit of work in A2A, with a lifecycle of states
//! - **Message**: Communication turns between client and agent
//! - **Part**: Polymorphic content (text, files, structured data)
//! - **Artifact**: Outputs generated by the agent during task execution
//!
//! All types implement `serde::Serialize` and `serde::Deserialize` for
//! JSON-RPC message transport, following the A2A specification's naming
//! conventions (camelCase for JSON, kebab-case for enum values).

use serde::{Deserialize, Serialize};

// ─── TaskState ─────────────────────────────────────────────────────────────────

/// Task lifecycle state — tracks the progression of an A2A Task.
///
/// The state machine defines these valid transitions:
/// - `Submitted` → `Working`
/// - `Working` → `Completed` | `Failed` | `InputRequired` | `Canceled`
/// - `InputRequired` → `Working`
/// - Any state → terminal (`Completed`, `Failed`, `Canceled`, `Rejected`)
///
/// Use `validate_transition()` to check if a state change is valid.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq, Hash)]
#[serde(rename_all = "kebab-case")]
pub enum TaskState {
    /// Task has been created/submitted but not yet started.
    Submitted,
    /// Agent is actively processing the task.
    Working,
    /// Agent needs additional input from the client to continue.
    InputRequired,
    /// Task has finished successfully with a result.
    Completed,
    /// Task was canceled (by client or agent).
    Canceled,
    /// Task encountered an error and could not complete.
    Failed,
    /// Agent rejected the task before starting work.
    Rejected,
}

impl std::fmt::Display for TaskState {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            TaskState::Submitted => write!(f, "submitted"),
            TaskState::Working => write!(f, "working"),
            TaskState::InputRequired => write!(f, "input-required"),
            TaskState::Completed => write!(f, "completed"),
            TaskState::Canceled => write!(f, "canceled"),
            TaskState::Failed => write!(f, "failed"),
            TaskState::Rejected => write!(f, "rejected"),
        }
    }
}

impl TaskState {
    /// Check whether this state is terminal (no further transitions possible).
    pub fn is_terminal(&self) -> bool {
        matches!(self, TaskState::Completed | TaskState::Failed | TaskState::Canceled | TaskState::Rejected)
    }

    /// Validate that a transition from `from` to `to` is allowed by the A2A spec.
    pub fn validate_transition(from: &TaskState, to: &TaskState) -> bool {
        match from {
            TaskState::Submitted => matches!(to, TaskState::Working | TaskState::Rejected | TaskState::Canceled),
            TaskState::Working => matches!(to, TaskState::Completed | TaskState::Failed | TaskState::InputRequired | TaskState::Canceled),
            TaskState::InputRequired => matches!(to, TaskState::Working | TaskState::Canceled | TaskState::Failed),
            // Terminal states — no further transitions allowed
            TaskState::Completed | TaskState::Failed | TaskState::Canceled | TaskState::Rejected => false,
        }
    }
}

// ─── Part (discriminated union) ────────────────────────────────────────────────

/// Polymorphic content part — the fundamental content unit in A2A messages and artifacts.
///
/// A Part can be:
/// - **Text**: Plain text content
/// - **File**: Binary/file content via URI or inline base64 bytes
/// - **Data**: Structured/arbitrary JSON data
///
/// Uses serde's internally tagged representation with `"type"` as the discriminant,
/// matching the A2A specification's `oneOf` schema pattern.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "type", rename_all = "camelCase")]
pub enum Part {
    /// Plain text content.
    Text {
        /// The text content.
        text: String,
    },
    /// File content — either a URI reference or inline base64 bytes.
    File {
        /// The file content descriptor.
        file: FileContent,
    },
    /// Structured/arbitrary key-value data payload.
    Data {
        /// The structured data (any JSON object).
        data: serde_json::Value,
    },
}

// ─── FileContent ───────────────────────────────────────────────────────────────

/// File content descriptor — provides either a URI reference or inline base64 bytes.
///
/// At least one of `uri` or `bytes` must be provided per the A2A spec.
/// The `mime_type` field is optional and describes the file's content type.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "camelCase")]
pub struct FileContent {
    /// URI reference to the file (optional — alternative to inline bytes).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub uri: Option<String>,
    /// Inline base64-encoded file bytes (optional — alternative to URI).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub bytes: Option<String>,
    /// MIME type of the file (e.g., "image/png", "application/pdf").
    #[serde(skip_serializing_if = "Option::is_none")]
    pub mime_type: Option<String>,
}

impl FileContent {
    /// Create a FileContent with a URI reference.
    pub fn from_uri(uri: String, mime_type: Option<String>) -> Self {
        Self { uri: Some(uri), bytes: None, mime_type }
    }

    /// Create a FileContent with inline base64 bytes.
    pub fn from_bytes(bytes: String, mime_type: Option<String>) -> Self {
        Self { uri: None, bytes: Some(bytes), mime_type }
    }
}

// ─── Message ───────────────────────────────────────────────────────────────────

/// A communication turn between the client and the agent during a task.
///
/// Each message has a `role` (who sent it) and a list of `Part` objects
/// (the content). Messages are the building blocks of task history.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Message {
    /// Who sent the message — `"user"` (client) or `"agent"` (remote agent).
    pub role: MessageRole,
    /// Content parts — polymorphic content units (text, files, data).
    pub parts: Vec<Part>,
    /// Optional extension metadata (key-value pairs).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub metadata: Option<serde_json::Value>,
}

/// Message role — identifies who sent the message.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub enum MessageRole {
    /// Message from the client (user).
    #[serde(rename = "user")]
    User,
    /// Message from the remote agent.
    #[serde(rename = "agent")]
    Agent,
}

impl Message {
    /// Create a simple user text message.
    pub fn user_text(text: impl Into<String>) -> Self {
        Self {
            role: MessageRole::User,
            parts: vec![Part::Text { text: text.into() }],
            metadata: None,
        }
    }

    /// Create a simple agent text message.
    pub fn agent_text(text: impl Into<String>) -> Self {
        Self {
            role: MessageRole::Agent,
            parts: vec![Part::Text { text: text.into() }],
            metadata: None,
        }
    }
}

// ─── TaskStatus ────────────────────────────────────────────────────────────────

/// Task status — the current state of a task, with optional context message.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct TaskStatus {
    /// The current lifecycle state.
    pub state: TaskState,
    /// Optional message providing context about the state change.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub message: Option<Message>,
    /// ISO 8601 timestamp of the state update.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub timestamp: Option<String>,
}

impl TaskStatus {
    /// Create a new TaskStatus with just a state.
    pub fn new(state: TaskState) -> Self {
        Self { state, message: None, timestamp: None }
    }

    /// Create a TaskStatus with state and context message.
    pub fn with_message(state: TaskState, message: Message) -> Self {
        Self { state, message: Some(message), timestamp: None }
    }
}

// ─── Task ───────────────────────────────────────────────────────────────────────

/// Task — the central unit of work in A2A.
///
/// A Task represents an ongoing or completed interaction between a client
/// agent and a remote agent. It tracks the lifecycle state, conversation
/// history, and any outputs (artifacts) produced.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Task {
    /// Unique task identifier (UUID).
    pub id: String,
    /// Optional session identifier to group related tasks.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub session_id: Option<String>,
    /// Current task status (state + optional message + timestamp).
    pub status: TaskStatus,
    /// Conversation history — messages exchanged during the task.
    /// Only available when the remote agent supports `stateTransitionHistory`.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub history: Vec<Message>,
    /// Outputs generated by the agent during task execution.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub artifacts: Vec<Artifact>,
    /// Optional extension metadata (key-value pairs).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub metadata: Option<serde_json::Value>,
}

impl Task {
    /// Create a new Task with the given ID and initial submitted state.
    pub fn new(id: impl Into<String>) -> Self {
        Self {
            id: id.into(),
            session_id: None,
            status: TaskStatus::new(TaskState::Submitted),
            history: Vec::new(),
            artifacts: Vec::new(),
            metadata: None,
        }
    }

    /// Check whether the task is in a terminal state.
    pub fn is_terminal(&self) -> bool {
        self.status.state.is_terminal()
    }

    /// Attempt a state transition. Returns Ok(new_state) if valid, Err if invalid.
    pub fn transition_to(&mut self, new_state: TaskState) -> Result<TaskState, crate::error::A2AError> {
        if !TaskState::validate_transition(&self.status.state, &new_state) {
            return Err(crate::error::A2AError::InvalidStateTransition {
                from: self.status.state.clone(),
                to: new_state,
            });
        }
        self.status.state = new_state.clone();
        Ok(new_state)
    }

    /// Add an artifact to the task.
    pub fn add_artifact(&mut self, artifact: Artifact) {
        self.artifacts.push(artifact);
    }

    /// Add a message to the task history.
    pub fn add_message(&mut self, message: Message) {
        self.history.push(message);
    }
}

// ─── Artifact ───────────────────────────────────────────────────────────────────

/// Artifact — a generated output/product from a task.
///
/// Artifacts are distinct from intermediate messages — they represent the
/// final outputs of the task (e.g., generated files, reports, data).
///
/// For streaming, artifacts use `index`, `append`, and `lastChunk` fields
/// to represent incremental chunks of a larger output.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Artifact {
    /// Position index in a streaming sequence (0-based).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub index: Option<usize>,
    /// Whether this artifact chunk appends to the previous one (streaming).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub append: Option<bool>,
    /// Whether this is the final chunk of the artifact (streaming).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub last_chunk: Option<bool>,
    /// Content parts — the same polymorphic Part types as Messages.
    #[serde(default)]
    pub parts: Vec<Part>,
    /// Optional human-readable name.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub name: Option<String>,
    /// Optional description.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub description: Option<String>,
    /// Optional extension metadata.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub metadata: Option<serde_json::Value>,
}

impl Artifact {
    /// Create a simple text artifact.
    pub fn text(name: impl Into<String>, text: impl Into<String>) -> Self {
        Self {
            index: None,
            append: None,
            last_chunk: None,
            parts: vec![Part::Text { text: text.into() }],
            name: Some(name.into()),
            description: None,
            metadata: None,
        }
    }

    /// Create a streaming artifact chunk.
    pub fn streaming_chunk(index: usize, parts: Vec<Part>, append: bool, last_chunk: bool) -> Self {
        Self {
            index: Some(index),
            append: Some(append),
            last_chunk: Some(last_chunk),
            parts,
            name: None,
            description: None,
            metadata: None,
        }
    }
}

